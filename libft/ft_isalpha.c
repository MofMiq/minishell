#include "libft.h"

int	ft_isalpha(int i)
{
	if (!((i >= 'a' && i <= 'z') || (i >= 'A' && i <= 'Z')))
		return (0);
	return (1);
}

int	ft_isalpha_plus(int i)
{
	if (!((i >= 'a' && i <= 'z') || (i >= 'A' && i <= 'Z')
			|| i == '_'))
		return (0);
	return (1);
}
